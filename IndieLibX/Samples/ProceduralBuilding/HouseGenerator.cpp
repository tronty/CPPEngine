#include <Framework3/HouseGenerator.h>

HouseGenerator* houseGenerator=0;

int init(const char* aTitle)
{
LOG_FNLN;
	houseGenerator=new HouseGenerator(3,3);
LOG_FNLN;
	houseGenerator->generate();
LOG_FNLN;
	return 0;
}

void render()
{
	float f=128.0f/256.0f;
	IRenderer::GetRendererInstance()->Clear(true, true, D3DXFROMWINEVECTOR4 (f, f, f, 1.0f));
	IRenderer::GetRendererInstance()->BeginScene();
LOG_FNLN;
	houseGenerator->render();
LOG_FNLN;
	IRenderer::GetRendererInstance()->EndScene();
	IRenderer::GetRendererInstance()->Present( );
}

int ApplicationLogic()
{
	IRenderer* r=IRenderer::GetRendererInstance("HouseGenerator");	
	IInput*    i=STX_Service::GetInputInstance();
	init("");
	while (!i->OnKeyPress (STX_KEY_ESCAPE) && !i->Quit())
	{
		i->Update();
		render();
	}
	delete houseGenerator;
	return 0;
}

